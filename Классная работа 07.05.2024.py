# Запрашиваем количество минут, прошедших с 0 часов
minutes = int(input("Введите количество минут, прошедших с 0 часов: "))

# Вычисляем количество часов
hours = minutes // 60

# Вычисляем оставшееся количество минут
remaining_minutes = minutes % 60

# Выводим результаты
print(f"Время: {hours} часов {remaining_minutes} минут")
