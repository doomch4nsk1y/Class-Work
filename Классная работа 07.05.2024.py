#Задание 4 
import math

# Ввод положительного вещественного числа A
A = float(input("Введите положительное вещественное число A: "))

# Определение целой и дробной части числа A
whole_part = int(A)
fractional_part = A - whole_part

# Вычисление арифметического квадратного корня числа A
sqrt_value = math.sqrt(A)

# Определение остатка от деления числа A на 5
remainder = A % 5

# Вывод результатов исследования
print(f"Целая часть числа A: {whole_part}")
print(f"Дробная часть числа A: {fractional_part}")
print(f"Значение арифметического квадратного корня числа A: {sqrt_value}")
print(f"Остаток от деления числа A на 5: {remainder}")

